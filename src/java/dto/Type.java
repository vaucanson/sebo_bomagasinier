package dto;

import java.io.Serializable;

/**
 *
 * @author djenadi
 */
public class Type implements Serializable {

    private String name;


    public Type() {
    }

    public Type(String name) {
        this.name = name;
    }

    
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    

    @Override
    public String toString() {
        return "Type{" + "name=" + name +  '}';
    }
    
    
    
}
